/* ─── Helpers ────────────────────────────────────── */
function gjjGet(lang, dotKey) {
  return dotKey.split('.').reduce((o, k) => (o && o[k] !== undefined ? o[k] : undefined), GJJ_I18N[lang]);
}

/* ─── Apply translations to the DOM ─────────────── */
function gjjApply(lang) {
  if (!GJJ_I18N[lang]) lang = 'en';

  // Plain text
  document.querySelectorAll('[data-i18n]').forEach(function (el) {
    var val = gjjGet(lang, el.dataset.i18n);
    if (val !== undefined) el.textContent = val;
  });

  // HTML (headings with <span class="gold">, hours paragraph)
  document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
    var val = gjjGet(lang, el.dataset.i18nHtml);
    if (val !== undefined) el.innerHTML = val;
  });

  // Active button state
  document.querySelectorAll('.lang-btn').forEach(function (btn) {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // Persist & set html[lang]
  try { localStorage.setItem('gjj-lang', lang); } catch (e) { }
  document.documentElement.lang = lang === 'fi' ? 'fi' : lang === 'sv' ? 'sv' : 'en';
}

/* ─── Init ───────────────────────────────────────── */
document.addEventListener('DOMContentLoaded', function () {
  var saved = 'en';
  try { saved = localStorage.getItem('gjj-lang') || 'en'; } catch (e) { }
  gjjApply(saved);

  document.querySelectorAll('.lang-btn').forEach(function (btn) {
    btn.addEventListener('click', function () { gjjApply(btn.dataset.lang); });
  });
});
